/**
 *
 * @param {data of current month} getSingleData
 * @returns [Generated table row]
 */
function generateData(getSingleData) {
    // remove old row
    container.innerHTML = "";
    // create table row
    if (getSingleData.length > 0) {
        getSingleData[0].data.map((monthData) => {
            let tr = document.createElement("tr");
            for (const [key, value] of Object.entries(monthData)) {
                let td = document.createElement("td");
                td.innerHTML = adjustTimeValue(key, value, monthData);
                tr.appendChild(td);
            }
            container.appendChild(tr);
            setActiveClass();
        });
    } else {
        let p = document.createElement("p");
        p.innerHTML = "No data found";
        container.appendChild(p);
    }
}

/**
 *
 * Set active class in
 *
 * Current date row;
 * Button
 */
function setActiveClass() {
    // Active table row of current date
    let allRowTd = document.querySelectorAll("#dateContainer2 td");
    allRowTd.forEach((elem) => {
        if (elem.innerHTML == today()) {
            elem.parentNode.classList.add("active");
        }
    });

    // Active button
    let selectedBtn = document.getElementById(container.dataset.month);
    selectedBtn.classList.add("active");
}

